    private void txtTeleponPengirimKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTeleponPengirimKeyTyped
        char Char = evt.getKeyChar();
        if (!((Char >= '0') && (Char <= '9') || (Char == KeyEvent.VK_BACK_SPACE) || (Char == KeyEvent.VK_DELETE) || (Char == '+'))) {
            JOptionPane.showMessageDialog(rootPane, "ERROR, MASUKAN ANGKA", "KESALAHAN", JOptionPane.INFORMATION_MESSAGE);
            evt.consume();
        }
        if (txtTeleponPengirim.getText().length() > 12) {
            JOptionPane.showMessageDialog(rootPane, "Kesalahan, Masukan 11-12 Digit nomor telepon", "Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_txtTeleponPengirimKeyTyped

